	package pack2;

	public class MathExample {
	public static void main(String[] args) {
		System.out.println(Math.PI);
		System.out.println(Math.E);
		System.out.println(Math.sqrt(25));//sqaure root 
		System.out.println(Math.sin(90));//
		System.out.println(Math.floor(10.9));//round of to the ground value 10
		System.out.println(Math.ceil(10.51));//round of to the topmost value 11
		System.out.println(Math.round(10.45));//if < 5 rounf off to ground ,if >=5 round
		System.out.println(Math.pow(2,4));//2 ^4 16
		System.out.println(Math.random());//returns a random value
		System.out.println(Math.max(23,45)); //45
		System.out.println(Math.min(34,12)); //12
	}
	}


