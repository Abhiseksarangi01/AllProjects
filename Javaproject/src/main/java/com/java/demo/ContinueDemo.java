package com.java.demo;

public class ContinueDemo {
public static void main(String[] args) {
	for(int i=0;i<=15;i++) {
		//check the condition for the continue
		if(i==10) {
			continue;//using continue to skip the execution of loop when i==10
		}
	//printing the elements to show continue 
	System.out.println(i +" ");
	
}
}
}